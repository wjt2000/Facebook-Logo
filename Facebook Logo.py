import turtle
turtle.speed(1)
turtle.penup()
turtle.goto(-100,-100)
turtle.pendown()
turtle.begin_fill()
turtle.pencolor("darkblue")
turtle.width(40)
turtle.fd(300)
turtle.left(90)
turtle.fd(300)
turtle.left(90)
turtle.fd(300)
turtle.left(90)
turtle.fd(300)
turtle.left(90)
turtle.fillcolor("darkblue")
turtle.end_fill()
turtle.fd(140)
turtle.penup()
turtle.right(90)
turtle.fd(120)
turtle.left(90)
turtle.pendown()
turtle.pencolor("white")
turtle.write('f',font=('sans',289,'bold'))
turtle.hideturtle()
